#ifndef MATHF_H__
#define MATHF_H__

/// \brief greater value between two values
/// \param a: first value to compare to the second
/// \param b: second value to compare to the first
/// \return the greater value
float max(float a, float b);

/// \brief smaller value between two values
/// \param a: first value to compare to the second
/// \param b: second value to compare to the first
/// \return the smaller value
float min(float a, float b);

/// \brief greatest value in a table
/// \param tab: table of integers
/// \param sizeTab: size of the integer table
/// \return the greatest value in a table
int maxInArray(int tab[], int sizeTab);

#endif

